function mostrarImagenes() {
    var div = document.getElementById("mi-div0");
    if (div.style.display === "none") {
        div.style.display = "block";
    } else {
        div.style.display = "none";
    }
}
function verEmpleado() {
    var div = document.getElementById("mi-div1");
    if (div.style.display === "none") {
        div.style.display = "block";
    } else {
        div.style.display = "none";
    }
}